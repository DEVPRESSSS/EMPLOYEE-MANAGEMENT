    private void ValidatePasswordMatching(){
        
        try {
            String passwordOne = Password.getText();
            String passwordTwo = ConfirmPassword.getText();
            
            if (passwordOne.isEmpty() || passwordTwo.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill in all fields.");
                Password.setText("");
                ConfirmPassword.setText("");
                return;
            }
            
            // Check if passwords match
            if (!passwordOne.equals(passwordTwo)) {
                JOptionPane.showMessageDialog(null, "Passwords do not match!");
                ConfirmPassword.setText("");
                Password.setText("");

                return;
            }
            
            
            Connection con = Database.getConnection();
            String sql = "UPDATE appusers SET Password = ? WHERE email = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,passwordTwo );
            pst.setString(2, _email);

            int rowsAffected = pst.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Password changed successfully!");
                this.dispose();
                new Login().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Failed to change password. Email not found.");
            }

            pst.close();
        } catch (SQLException ex) {
            Logger.getLogger(ChangePassword.class.getName()).log(Level.SEVERE, null, ex);
        }

        

        
        
    }
 
